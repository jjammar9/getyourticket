export function getCountryBySlug(slug) {
  return countries[slug] || null;
}

export function getCountryByCity(city) {
  for (const entry of Object.values(countries)) {
    if (entry.cities.some((c) => c.toLowerCase() === city.toLowerCase())) {
      return entry;
    }
  }
  return null;
}
